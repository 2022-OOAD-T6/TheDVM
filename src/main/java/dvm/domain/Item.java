package dvm.domain;

/**
 * 
 */
public class Item {

    /**
     * Default constructor
     */
    public Item() {
    }

    /**
     * 
     */
    private String itemCode;

    /**
     * 
     */
    private String name;

    /**
     * 
     */
    private int price;

    public String getItemCode() {
        return itemCode;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }
}